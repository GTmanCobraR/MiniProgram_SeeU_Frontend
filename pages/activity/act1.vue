<template>
  <view class="page">
    <!-- 顶部渐变背景 -->
    <view
      class="top-gradient"
      :style="{ height: (statusBarHeight + titleBarHeight + gradientExtra) + 'px' }"
    ></view>

    <!-- 背景大字 SEEU -->
    <text class="bg-see-u">SEEU</text>

    <!-- 自定义导航栏（透明，含返回按钮 + 标题） -->
    <view
      class="custom-nav"
      :style="{ height: navHeight + 'px', paddingTop: statusBarHeight + 'px' }"
    >
      <view class="nav-inner">
        <!-- 返回按钮 -->
        <view v-if="canGoBack" class="back-btn" @tap="goBack">
          <text class="back-arrow">‹</text>
        </view>
        <text class="nav-title">问卷</text>
      </view>
    </view>

    <!-- 内容区：整体下移 navHeight，避免被覆盖 -->
    <view class="card-list" :style="{ marginTop: navHeight + 'px' }">
      <view
        v-for="(item, index) in surveys"
        :key="index"
        class="card"
        @tap="openSurvey(item.activityId)"
      >
        <text>{{ item.title }}</text>
      </view>
    </view>
	
	<image
	  class="bottom-logo"
	  src="@/static/decorations/survey_logo.png"
	  mode="widthFix"
	></image>
  </view>
</template>

<script>
export default {
  data() {
    const sys = uni.getSystemInfoSync();
    const statusBarHeight = sys.statusBarHeight || 20;
    const titleBarHeight = 44;
    return {
      surveys: [
        { title: "问卷A", activityId: "Q1w1pE3" },
        { title: "问卷B", activityId: "Q1w1pE3" },
        { title: "问卷C", activityId: "Q1w1pE3" }
      ],
      statusBarHeight,
      titleBarHeight,
      navHeight: statusBarHeight + titleBarHeight,
      gradientExtra: 160,
      canGoBack: false
    };
  },
  onShow() {
    const pages = getCurrentPages();
    this.canGoBack = pages && pages.length > 1;
  },
  methods: {
    goBack() {
      const pages = getCurrentPages();
      if (pages.length > 1) {
        uni.navigateBack({ delta: 1 });
      } else {
        // 如果是首页或tab页，不可返回，可改成跳首页
        uni.switchTab({ url: '/pages/index/index' });
      }
    },
    openSurvey(activityId) {
      const memberId = uni.getStorageSync('memberId') || '';
      const token = uni.getStorageSync('token') || '';
      if (!memberId || !token) {
        uni.showToast({ title: '请先登录', icon: 'none' });
        return;
      }
      uni.navigateToMiniProgram({
        appId: 'wxd947200f82267e58',
        path: `pages/wjxqList/wjxqList?activityId=${activityId}`
      });
    }
  }
};
</script>

